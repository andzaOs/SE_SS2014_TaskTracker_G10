package Izvjestaj;

import java.util.Date;

public class stavkaIzvjestajaRadnik {
	String klijent;
	String vrstaUsluge;
	String datum;
	String brojSati;
	
	public stavkaIzvjestajaRadnik() {
		
	}
	
	public String getKlijent() {
		return klijent;
	}
	public void setKlijent(String klijent) {
		this.klijent = klijent;
	}
	public String getVrstaUsluge() {
		return vrstaUsluge;
	}
	public void setVrstaUsluge(String vrstaUsluge) {
		this.vrstaUsluge = vrstaUsluge;
	}
	public String getDatum() {
		return datum;
	}
	public void setDatum(String datum) {
		this.datum = datum;
	}
	public String getBrojSati() {
		return brojSati;
	}
	public void setBrojSati(String brojSati) {
		this.brojSati = brojSati;
	}
	
	
	
}
