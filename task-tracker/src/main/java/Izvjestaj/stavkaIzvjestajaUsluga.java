package Izvjestaj;

public class stavkaIzvjestajaUsluga {
	String radnik;
	String klijent;
	String datum;
	String brojSati;
	
	public stavkaIzvjestajaUsluga() {}

	public String getRadnik() {
		return radnik;
	}

	public void setRadnik(String radnik) {
		this.radnik = radnik;
	}

	public String getKlijent() {
		return klijent;
	}

	public void setKlijent(String klijent) {
		this.klijent = klijent;
	}

	public String getDatum() {
		return datum;
	}

	public void setDatum(String datum) {
		this.datum = datum;
	}

	public String getBrojSati() {
		return brojSati;
	}

	public void setBrojSati(String brojSati) {
		this.brojSati = brojSati;
	};
	
	

}
