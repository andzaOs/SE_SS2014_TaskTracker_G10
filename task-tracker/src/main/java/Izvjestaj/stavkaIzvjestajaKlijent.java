package Izvjestaj;

public class stavkaIzvjestajaKlijent {
	String radnik;
	String usluga;
	String datum;
	String brojSati;
	
	public stavkaIzvjestajaKlijent() {};
	
	public String getRadnik() {
		return radnik;
	}
	public void setRadnik(String radnik) {
		this.radnik = radnik;
	}
	public String getUsluga() {
		return usluga;
	}
	public void setUsluga(String usluga) {
		this.usluga = usluga;
	}
	public String getDatum() {
		return datum;
	}
	public void setDatum(String datum) {
		this.datum = datum;
	}
	public String getBrojSati() {
		return brojSati;
	}
	public void setBrojSati(String brojSati) {
		this.brojSati = brojSati;
	}

}
